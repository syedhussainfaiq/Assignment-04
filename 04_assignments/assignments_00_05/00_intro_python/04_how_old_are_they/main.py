def solve_age_riddle():
    Anthon = 21
    Beth = Anthon + 6
    Chen = Beth + 20
    Drew = Chen + Anthon
    Ethen = Chen
    
    print(f"Anthon is {Anthon} years old")
    print(f"Beth is {Beth} years old")
    print(f"Chen is {Chen} years old")
    print(f"Drew is {Drew} years old")
    print(f"Ethen is {Ethen} years old")

if __name__ == "__main__":
    solve_age_riddle()    