def sum_of_numbers(numbers):
    """
    Takes a list of numbers and returns their sum.
    
    Parameters:
        numbers (list): A list of numerical values.
    
    Returns:
        int/float: The sum of the numbers in the list.
    """
    return sum(numbers)

# Example usage:
numbers = [1, 2, 3, 4, 5]
result = sum_of_numbers(numbers)
print(f"The sum of {numbers} is {result}.")
